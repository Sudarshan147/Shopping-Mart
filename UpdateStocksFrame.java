  private void loadProductsDetails() {
        try
      {
          List<ProductsPojo> al = ProductsDAO.getProductDetails();
          if(al.isEmpty())
          {
                JOptionPane.showMessageDialog(null,"No Item present in the Stocks","Information",JOptionPane.INFORMATION_MESSAGE);
                 return;
          }
          btnUpdate.setEnabled(true);
          Object [] rows = new  Object[7];
          tm= (DefaultTableModel)jtProductList.getModel();
          for(ProductsPojo p:al)
          {
              rows[0]= p.getProductId();
              rows[1]= p.getProductName();
              rows[2]= p.getProductsPrice();
              rows[3]= p.getOurPrice();
              rows[4]= p.getProductCompony();
              rows[5]=p.getQuantity();
              rows[6]=p.getTax()+"%";
              tm.addRow(rows);
          }
          
      }
       catch(SQLException ex)
       {
          JOptionPane.showMessageDialog(null,"DataBase error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();  
       }
    }

