    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      int result =validateInputs();
      if(result==0)
      {
           JOptionPane.showMessageDialog(null,"Password must be of 4 letters","Error",JOptionPane.ERROR_MESSAGE);
           return;
      }
      else if(result==-1)
              {
                 JOptionPane.showMessageDialog(null,"user id can't be blank","Error",JOptionPane.ERROR_MESSAGE);  
                 return;
              }
      char []pwd =jPassword.getPassword();
      char []repwd = jRePassword.getPassword();
      String spwd = String.valueOf(pwd);
      String srepwd =String.valueOf(repwd);
      if(isPasswordMatching(spwd,srepwd)==false)
      {
           JOptionPane.showMessageDialog(null,"Password do not match","Error",JOptionPane.ERROR_MESSAGE);
           return;
      }
      try
      {
        String empid= jcEmpId.getSelectedItem().toString();
        String userid = txtUserId.getText().trim();
        String username = txtEmpName.getText();
        UserPojo u = new UserPojo();
        u.setEmpid(empid);
        u.setUserid(userid);
        u.setUsername(username);
        u.setPassword(spwd);
        u.setUsertype("Receptionist");
        boolean status = ReceptionistDAO.addReceptionist(u);
        if(status)
        {
             JOptionPane.showMessageDialog(null,"Receptionist added succesfully");
             clearText();
             return;
        }
         JOptionPane.showMessageDialog(null,"Receptionist not added");
         loadReceptionistDetails();
         return;
        
        
      }
       catch(SQLException ex)
          {
            JOptionPane.showMessageDialog(null,"DataBase error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_btnAddActionPerformed

