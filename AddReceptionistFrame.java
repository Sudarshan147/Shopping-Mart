    private void loadReceptionistDetails() {
       try
       {
           receptionist = ReceptionistDAO.getNonRegisterReceptionists();
           if(receptionist.isEmpty())
           {
                 JOptionPane.showMessageDialog(null,"no un-registered receptionist is present");
                 btnAdd.setEnabled(false);
                 return;
           }
           btnAdd.setEnabled(true);
           Set keys = receptionist.keySet();
           Iterator<String> it= keys.iterator();
           jcEmpId.removeAllItems();
           while(it.hasNext())
           {
               jcEmpId.addItem(it.next());
           }
           
       }
          catch(SQLException ex)
          {
            JOptionPane.showMessageDialog(null,"DataBase error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }

